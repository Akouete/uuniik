function AttachEmoji(btn, inputid) {
  var btn = document.querySelector(btn);
  var inputid = document.querySelector(inputid);
  var emo=document.querySelectorAll('.emo');
  var emojivisible = 1;
  btn.addEventListener('click', function() {
    if(emojivisible == 1) {
      $('.emoji').removeClass('none');
      for (var i = 0; i < emo.length; i++) {
        emo[i].onclick=function() {
          inputid.value+=this.innerText;
        }
      }
      emojivisible = 0;
    }else {
      $('.emoji').addClass('none');
      emojivisible = 1;
    }
  }, false);
}

